    /**
     * 每length个base64字符解密一次，然后合并字符串输出
     *
     * @param base64
     * @param length长度
     * @param privateKey
     * @return
     * @throws Exception
     */
    public static String decryptByPrivateKey(String base64, int length, PrivateKey privateKey) throws Exception {
        StringBuffer sb = new StringBuffer(base64);
        StringBuffer resu = new StringBuffer();

        int pagesize = length;
        int len = sb.length();
        int totalPage = (len / pagesize);
        String curr = "";
        for (int i = 0; i < totalPage; i++) {
            if (i == (totalPage - 1))
                curr = base64.substring(i * pagesize);
            else
                curr = base64.substring(i * pagesize, (i + 1) * pagesize);
            resu.append(new String(
                    decryptByPrivateKey(Base64Utils.decode(curr), privateKey)
                    , "utf-8"));
        }
        return resu.toString();
    }

    /**
     * <P>
     * 私钥解密
     * </p>
     *
     * @param encryptedData 已加密数据
     * @param privateKey    私钥(BASE64编码)
     * @return
     * @throws Exception
     */
    public static byte[] decryptByPrivateKey(byte[] encryptedData, PrivateKey privateKey) throws Exception {
//		KeyFactory keyFactory = KeyFactory.getInstance(RSA);
        Cipher cipher = Cipher.getInstance(V);
        cipher.init(Cipher.DECRYPT_MODE, privateKey);
        int inputLen = encryptedData.length;
        ByteArrayOutputStream out = new ByteArrayOutputStream();
        int offSet = 0;
        byte[] cache;
        int i = 0;
        // 对数据分段解密
        while (inputLen - offSet > 0) {
            if (inputLen - offSet > MAX_DECRYPT_BLOCK) {
                cache = cipher.doFinal(encryptedData, offSet, MAX_DECRYPT_BLOCK);
            } else {
                cache = cipher.doFinal(encryptedData, offSet, inputLen - offSet);
            }
            out.write(cache, 0, cache.length);
            i++;
            offSet = i * MAX_DECRYPT_BLOCK;
        }
        byte[] decryptedData = out.toByteArray();
        out.close();
        return decryptedData;
    }

